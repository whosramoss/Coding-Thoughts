'''
The main types of data used in Python are: int, float, bool, complex and str.
'''

#declaration of variables
a = 3
b = 10
c = 100
d = 1000

print("sum = " + str(a + b + c + d))

subtraction = d - c - b - a
print("subtraction = " + str(subtraction))

division = d / c
print("division = " + str(division))

multiplication = a * b
print("multiplication = " + str(multiplication))

exponential = a ** 2
print("exponential = " + str(exponential))

remainder = 3 % 2
print("remainder = " + str(remainder))